from pyBitwiseAutomation import *
import sys
import math

def test_Pela(ip_address: str, stopOnError: bool, run: int):
    Pela = PelaDevice()
    try:
        Pela.Connect(ip_address)

        serialNumber = Pela.Const.getSN()

        print("PELA TEST PROGRAM")
        print("Run value........."+str(run))
        print("IP Address........"+ip_address)
        print("Serial number....."+serialNumber)
        print("Build............."+Pela.Sys.getBuild())
        print("Architecture......" + Pela.Sys.getArchitecture())
        print("StopOnError......."+str(stopOnError))
        print("Clock rate........"+str(Pela.ED.getClockRateGHz()))
        print("Clock type........"+str(Pela.ED.getClockType()))
        print("Link mode........."+str(Pela.ED.getLinkMode()))
        print("Ch0..............."+str(Pela.ED.getDetPatt(0)))
        print("Ch1..............."+str(Pela.ED.getDetPatt(1)))
        print("Temperature......."+str(Pela.getTemperatureC())+" C")

    finally:
        Pela.Disconnect()
        Pela = None

    return None

if __name__ == '__main__':
    print("TestPela, Version 1.1\n")

    stopOnError = False
    ipCount = 0
    repeat = 1
    ip = [32]

    i = 1
    while i<len(sys.argv):
        if sys.argv[i] == "-stop" :
            stopOnError = True
        elif sys.argv[i] == "-repeat":
            repeat = int(sys.argv[i + 1])
            i = i+1
        elif ipCount<32:
            ip[ipCount] = sys.argv[i]
            ipCount = ipCount+1
        else:
            print( "Too many IP addresses, maximum is 32")
            exit()

        i = i+1

    if ipCount == 0 or repeat < 1 :
        print("Usage:  TestPela [options] IP0 IP1 ... IPn")
        print("Options:  -stop ..... stop on first error")
        print("          -repeat N.. number of tests for each IP")
        exit()

    try:
        for ip_address in ip:
            for k in range(1, repeat+1):
                test_Pela(ip_address, stopOnError, k)

    except KeyboardInterrupt:
        print("\nCtrl-C encountered")

